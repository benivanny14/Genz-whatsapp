// Real-time Transcription Component
export const RealTimeTranscription = ({ isRecording, onTranscriptUpdate }) => {
  const [liveTranscript, setLiveTranscript] = useState('');
  const [isProcessing, setIsProcessing] = useState(false);

  useEffect(() => {
    if (isRecording) {
      // Simulate real-time transcription
      const interval = setInterval(() => {
        const words = ['Hello', 'this', 'is', 'a', 'test', 'of', 'real-time', 'transcription'];
        const randomWord = words[Math.floor(Math.random() * words.length)];
        setLiveTranscript(prev => prev + ' ' + randomWord);
        
        if (onTranscriptUpdate) {
          onTranscriptUpdate(liveTranscript + ' ' + randomWord);
        }
      }, 1000);

      return () => clearInterval(interval);
    }
  }, [isRecording]);

  return (
    <div className="bg-[#0b141a]/50 rounded-lg p-3">
      <div className="flex items-center gap-2 mb-2">
        <div className={`w-2 h-2 rounded-full ${isRecording ? 'bg-red-500 animate-pulse' : 'bg-gray-500'}`} />
        <span className="text-gray-400 text-xs">
          {isRecording ? 'Live transcription' : 'Transcription paused'}
        </span>
      </div>
      {liveTranscript && (
        <p className="text-gray-300 text-sm">{liveTranscript}</p>
      )}
    </div>
  );
};

